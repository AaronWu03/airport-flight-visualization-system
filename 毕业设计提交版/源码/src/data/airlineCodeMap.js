// 获取航司名称的工具函数
export function getAirlineName(code) {
  if (!code) return '未知航司'
  return airlineNameMap[code.toUpperCase()] || code
}

// 获取航司二字码（从全称反向查找）
export function getAirlineCode(name) {
  if (!name) return ''
  const upperName = name.toUpperCase()
  for (const [code, fullName] of Object.entries(airlineNameMap)) {
    if (fullName.toUpperCase().includes(upperName)) {
      return code
    }
  }
  return ''
}

